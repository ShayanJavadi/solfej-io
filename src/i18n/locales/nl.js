<p>Als Suno een akkoord genereert dat niet in deze lijst staat, gaat het "buiten de toonsoort" — wat opzettelijk en gaaf kan klinken, of willekeurig en slecht. Het specificeren van de toonsoort in je prompt vermindert de willekeurigheid.</p>
